	$cur_tm = time();
	$dif = $cur_tm-$tm;
	$pds = array('second', 'minute', 'hour', 'day', 'week', 'month', 'year', 'decade');
	$lngh = array(1, 60, 3600, 86400, 604800, 2630880, 31570560, 315705600);
